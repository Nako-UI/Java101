public class ifElse {
    public static void main(String[] args) {
        int var2 = 10;
        int var3 =3;
        if (var2==var3){
            System.out.println(5);
        } else if (var2<var3) {
            System.out.println(10);
        }
        else {
            System.out.println(100);
        }


    }
}
